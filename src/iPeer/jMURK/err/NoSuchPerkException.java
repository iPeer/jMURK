package iPeer.jMURK.err;

@SuppressWarnings("serial")
public class NoSuchPerkException extends Exception {

	public NoSuchPerkException() {
	}

	public NoSuchPerkException(String arg0) {
		super(arg0);
	}

	public NoSuchPerkException(Throwable arg0) {
		super(arg0);
	}

	public NoSuchPerkException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
